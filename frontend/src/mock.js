// Mock search function
export const searchProperties = (filters) => {
  let filtered = [...mockProperties];
  
  if (filters.priceRange) {
    filtered = filtered.filter(p => p.price >= filters.priceRange[0] && p.price <= filters.priceRange[1]);
  }
  
  if (filters.type && filters.type !== 'all') {
    filtered = filtered.filter(p => p.type.toLowerCase() === filters.type.toLowerCase());
  }
  
  if (filters.capacity) {
    const capacity = parseInt(filters.capacity);
    filtered = filtered.filter(p => parseInt(p.capacity) >= capacity);
  }
  
  return filtered;
};

